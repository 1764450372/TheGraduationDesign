package domain;

public class TravelNews {
	
	private int id;
    private String city;
    private String neswContent;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getNeswContent() {
		return neswContent;
	}
	public void setNeswContent(String neswContent) {
		this.neswContent = neswContent;
	}


}
